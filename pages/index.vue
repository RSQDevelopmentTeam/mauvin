          document.querySelector('#sound-toggle').classList.remove('off')
          document.querySelector('#sound-toggle').classList.add('on')
        }
      })
    }, 222)



    this.animateIn(this.finishedAnimating);
  },
  created: function() {},
  methods: {
    showTrigger() {
      alert(this.$store.state.mauvinSettings.showCursorsProxyNum)
      this.$store.commit('mauvinSettings/showTrigger', !this.$store.state.mauvinSettings.showCursorsProxyNum);
      if (this.$store.state.mauvinSettings.showCursorsProxyNum) {
        document.querySelectorAll('.pythagoreantheorem').forEach((elm) => {
          elm.classList.remove('show')
        })
      } else {
        document.querySelectorAll('.pythagoreantheorem').forEach((elm) => {
          elm.classList.add('show')
        })
      }

    },
    toggleStroke() {
      document.querySelector('.js-toggle-stroke').classList.toggle('is-on');
      this.$store.commit('mauvinSettings/strokeCursor', this.$store.state.mauvinSettings.stroke.strokeCursor);
    },
    updateCursorColor(e) {
      if (typeof e.target.dataset.color !== 'undefined' && !e.target.classList.contains('active')) {
        document.querySelector('#mauvin-color').style.setProperty('--color', e.target.dataset.color);
        this.$store.commit('mauvinSettings/cursorColor', e.target.dataset.color);
        document.querySelectorAll('.color-dot-container.mauvin .color-dot').forEach((elm) => {
          elm.classList.remove('active')
        })
        e.target.classList.add('active');

        document.querySelector('#grid').style.setProperty('--color', e.target.dataset.color);
        document.querySelectorAll('.js-updateColor').forEach((elm) => {
          elm.style.setProperty('--adpotColor', e.target.dataset.color);
        });

      }
    },
    updateStrokeColor(e) {
      if (typeof e.target.dataset.color !== 'undefined') {
        document.querySelector('#mauvin-stroke').style.setProperty('--color', e.target.dataset.color);
        document.querySelector('#mauvin-stroke-real').style.setProperty('--color', e.target.dataset.color);
        this.$store.commit('mauvinSettings/strokeCursorColor', e.target.dataset.color);
        document.querySelectorAll('.color-dot-container.stroke .color-dot').forEach((elm) => elm.classList.remove('active'))
        document.querySelectorAll('.js-updateStrokeColor').forEach((elm) => {
          elm.style.setProperty('--adpotStrokeColor', e.target.dataset.color);
        });
        e.target.classList.add('active')
      }
    },
    finishedAnimating: function() {
      this.locomotiveScrollInit();
    },
  }
};
